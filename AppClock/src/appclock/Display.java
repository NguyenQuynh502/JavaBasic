   private void clock(){
       new Thread(){
          public void run(){
              while(true){
                  Calendar ca = new GregorianCalendar();
                  int hour = ca.get(Calendar.HOUR);
                  int min = ca.get(Calendar.MINUTE);
                  int sec = ca.get(Calendar.SECOND);
                  int AM_PM = ca.get(Calendar.AM_PM);
                  
                  String day_night;
                  if (AM_PM == 1) {
                      day_night = "PM";
                      
                  } else {
                      day_night = "AM";
                  }
                  lbl_clock.setText(hour + ":"+ min +":"
                                    + sec + " "+ day_night );
              }
          }
       }.start();
       
   }
